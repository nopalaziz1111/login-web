            2. Matematika dalam Sejarah
            Matematika digunakan untuk menganalisis peristiwa sejarah, seperti:

            Demografi: Pertumbuhan populasi menggunakan persamaan eksponensial.

            Ekonomi Historis: Statistik perdagangan rempah abad ke-16.

            Kronologi: Menghitung waktu dengan sistem kalender (Gregorian vs Hijriyah).

            Contoh Aplikasi:

            Menghitung jarak tempuh pelayaran Columbus dengan trigonometri.

            Analisis data kependudukan Romawi Kuno menggunakan mean/deviasi.
