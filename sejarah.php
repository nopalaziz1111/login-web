        <h1>sejarah komunis</h1>
        <p>
            ## Asal-usul Teoritis (1840-an)
            Marx dan Engels mengembangkan teori komunisme sebagai kritik terhadap kapitalisme industrial. Dalam
            "Manifesto Komunis" (1848), mereka menggambarkan visi masyarakat tanpa kelas di mana alat-alat produksi
            dimiliki bersama.

            ## Revolusi Rusia (1917)
            Vladimir Lenin memimpin Revolusi Oktober yang menggulingkan pemerintahan sementara Rusia, menciptakan
            negara komunis pertama di dunia. Uni Soviet menjadi model bagi gerakan komunis global.

            ## Ekspansi Pasca-Perang Dunia II
            Setelah 1945, komunisme menyebar ke Eropa Timur, China (1949 di bawah Mao Zedong), Korea Utara, Vietnam,
            Kuba, dan negara-negara lain. Ini memicu Perang Dingin antara blok kapitalis dan komunis.

            ## Variasi dan Perpecahan
            Berbagai bentuk komunisme berkembang - Stalinisme di Soviet, Maoisme di China, dan model lain di
            Yugoslavia, Albania, dan tempat lain. Perbedaan interpretasi sering menyebabkan konflik antar-negara
            komunis.

            ## Kemunduran (1980-1990-an)
            Reformasi Gorbachev di Soviet, jatuhnya Tembok Berlin (1989), dan pembubaran Uni Soviet (1991) menandai
            berakhirnya era komunis di Eropa Timur. China beralih ke ekonomi pasar sosialis.

            ## Kondisi Saat Ini
            Beberapa negara seperti China, Vietnam, Laos, Kuba, dan Korea Utara masih mengklaim sebagai negara
            komunis, meski sebagian besar telah mengadopsi elemen ekonomi pasar.

            Komunisme tetap menjadi topik perdebatan akademis dan politik, dengan berbagai penilaian tentang dampak
            historisnya terhadap masyarakat dan ekonomi dunia.
        </p>
